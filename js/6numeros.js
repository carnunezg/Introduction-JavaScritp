//NOTA: Cuando en la console la palbra sale en azul quiere decir que es una variable de tipo números y se sale en negro es tipo texto o string.

const numero1 = 100;
const numero2 = 200;
const numero3 = 45;
const numero4 = 3;
const numero5 = 2;

