console.log(numero1 + numero2); //Resultado: 300
console.log(numero2 - numero1); //Resultado: 100
console.log(numero2 * numero1); //Resultado: 20000
console.log(numero2 / numero1); //Resultado: 2
console.log(numero4 ** numero5); //Este es el porcentaje, Resultado: 9
console.log(numero3 % numero4); //con el operador módulo te muestra el Residuo, es decir lo que te queda en la división, en este caso es 0 NOTA: Este operador módulo siempre lo usan en las entrevistas de trabajo. aunque no se usa mucho pero es bueno saberlo.

